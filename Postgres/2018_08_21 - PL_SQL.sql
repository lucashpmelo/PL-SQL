DO
$$
DECLARE
	SUM_SAL NUMERIC(10, 2);
	DEPTNO NUMERIC NOT NULL := 60;
BEGIN
	SELECT SUM(SALARY) INTO SUM_SAL FROM EMPLOYEES WHERE DEPARTMENT_ID = DEPTNO;
	RAISE NOTICE 'SOMA SALARIO %', SUM_SAL;
END;
$$

DO
$$
DECLARE
	TYPE EMP_REC_TYPE IS RECORD
		(CODIGO EMPLOYEES.EMPLOYEE_ID%TYPE,
		 NOME EMPLOYEES.FIRST_NAME%TYPE,
		 SOBRENOME EMPLOYEES.LAST_NAME%TYPE);
	EMP_REC EMP_REC_TYPE;
BEGIN
	SELECT EMPLOYEE_ID, FIRST_NAME, LAST_NAME INTO EMP_REC FROM EMPREGADO WHERE EMPLOYEE_ID = 124;
	RAISE NOTICE 'CODIGO: %, NOME: %, SOBRENOME: %', EMP_REC.CODIGO, EMP_REC.NOME, EMP_REC.SOBRENOME;	
END;
$$


$$
DECLARE
	EMP CURSOR (DEPTNO_ID EMPLOYEES.DEPARTMENT_ID%TYPE) IS SELECT EMPLOYEE_ID, LAST_NAME FROM EMPLOYEES WHERE DEPARTMENT_ID = DEPTNO_ID FOR UPDATE;
	EMP_RECORD RECORD;
BEGIN
	RAISE NOTICE 'DEPARTAMENTO 10';
	OPEN EMP(10);
	LOOP
		FETCH EMP INTO EMP_RECORD;
		EXIT WHEN NOT FOUND;
		RAISE NOTICE 'ID: %, NOME: %', EMP_RECORD.EMPLOYEE_ID, EMP_RECORD.LAST_NAME;
		UPDATE EMPLOYEES SET SALARY = 10 WHERE CURRENT OF EMP;
	END LOOP;
	CLOSE EMP;
END;
$$
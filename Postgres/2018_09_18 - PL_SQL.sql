DO
$$
DECLARE
	SAL CURSOR IS SELECT EMPLOYEE_ID, LAST_NAME FROM EMPLOYEES WHERE DEPARTMENT_ID = 30;
	EMPNO EMPLOYEES.EMPLOYEE_ID%TYPE;
	LNAME EMPLOYEES.LAST_NAME%TYPE;
BEGIN
	OPEN SAL;
	FETCH SAL INTO EMPNO, LNAME;
	RAISE NOTICE 'ID: %, NOME: %', EMPNO, LNAME;
	CLOSE SAL;
END;
$$

